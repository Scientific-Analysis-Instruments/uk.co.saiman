package uk.co.saiman.experiment;

public enum ExecutionStrategy {
  /**
   * The node may be executed independently of execution of ancestor nodes, so
   * long as all result have been produced for ancestors. Workspace
   * implementations may choose to re-execute such nodes automatically whenever
   * their configuration is changed.
   */
  RESULT_DATA_DEPENDENT,

  /**
   * The node is dependent on the context provided through the execution of the
   * parent node and so cannot be executed independently. If the parent node
   * invokes {@link ExecutionContext#executeChildren()} during its own execution,
   * this node will be executed at that time.
   */
  EXECUTION_STATE_DEPENDENT,

  FORBIDDEN;
}
