package uk.co.saiman.eclipse.ui;

import java.util.stream.Stream;

public interface TransferIn<T> {
  Stream<T> data();

  TransferInPosition position();

  /**
   * Get the tree entry adjacent to the drop position in the case of a
   * {@link TransferInPosition#BEFORE_CHILD} or {@link TransferInPosition#AFTER_CHILD}
   * drop position. Returns null in the case of {@link TransferInPosition#OVER}.
   * 
   * @return the adjacent object or else null
   */
  T adjacentItem();
}
